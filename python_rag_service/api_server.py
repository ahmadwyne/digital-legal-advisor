# ── Embedding helpers (BGE mean-pool + L2-norm, no sentence_transformers) ─────

def _mean_pool_normalize(model_output, attention_mask) -> np.ndarray:
    """Mean pooling over token embeddings, then L2 normalise."""
    mask  = attention_mask.unsqueeze(-1).float()
    pooled = (model_output.last_hidden_state * mask).sum(1) / mask.sum(1).clamp(min=1e-9)
    return F.normalize(pooled, p=2, dim=1).cpu().numpy().astype("float32")


def _encode_query(text: str) -> np.ndarray:
    """Encode a single query string into a 768-dim BGE embedding."""
    tok = _state["embed_tok"]
    mdl = _state["embed_mdl"]
    # BGE requires "query: " prefix for asymmetric retrieval
    enc = tok(
        ["query: " + text],
        padding=True,
        truncation=True,
        max_length=512,
        return_tensors="pt",
    )
    with torch.no_grad():
        out = mdl(**enc)
    return _mean_pool_normalize(out, enc["attention_mask"])   # shape (1, 768)


def _rerank_scores(query: str, docs: list[str]) -> list[float]:
    """Score (query, doc) pairs with the cross-encoder reranker."""
    tok = _state["rerank_tok"]
    mdl = _state["rerank_mdl"]
    feat = tok(
        [query] * len(docs),
        docs,
        padding=True,
        truncation=True,
        max_length=512,
        return_tensors="pt",
    )
    with torch.no_grad():
        logits = mdl(**feat).logits.squeeze(-1)
    return logits.tolist()


