# ── Auth ───────────────────────────────────────────────────────────────────────

def _verify_token(authorization: str | None) -> None:
    if not SERVICE_API_KEY:
        return
    if not authorization or not authorization.startswith("Bearer "):
        raise HTTPException(status_code=401, detail="Missing Authorization header")
    if authorization.split(" ", 1)[1].strip() != SERVICE_API_KEY:
        raise HTTPException(status_code=401, detail="Invalid API key")


# ── Retrieval ──────────────────────────────────────────────────────────────────

def _expand_query(query: str) -> str:
    q_lower = query.lower()
    added = []
    for term, syns in LEGAL_SYNONYMS.items():
        if term in q_lower and syns[0] not in q_lower and syns[0] not in added:
            added.append(syns[0])
        if len(added) >= 3:
            break
    return (query + " " + " ".join(added)) if added else query


def _retrieve(query: str, top_k: int) -> list:
    index  = _state["index"]
    chunks = _state["chunks"]
    bm25   = _state["bm25"]
    n      = len(chunks)

    search_q = _expand_query(query)

    # Dense retrieval
    q_emb    = _encode_query(search_q)          # (1, 768)
    k_search = min(50, n)
    dense_scores, dense_idx = index.search(q_emb, k_search)
    dense_full = np.zeros(n, dtype=np.float32)
    for s, i in zip(dense_scores[0], dense_idx[0]):
        if i >= 0:
            dense_full[int(i)] = float(s)

    # BM25 sparse retrieval
    tokens   = search_q.lower().split()
    bm25_raw = np.array(bm25.get_scores(tokens), dtype=np.float32)

    def _norm(a):
        mn, mx = a.min(), a.max()
        return (a - mn) / (mx - mn + 1e-10)

    hybrid     = HYBRID_ALPHA * _norm(dense_full) + (1 - HYBRID_ALPHA) * _norm(bm25_raw)
    ranked_idx = np.argsort(hybrid)[::-1]

    # Diversity: max 2 chunks per section
    candidates     = []
    section_counts = Counter()
    for idx in ranked_idx:
        c   = chunks[int(idx)]
        key = f"{c.get('act_title','')}__sec{c.get('section_number','')}"
        if section_counts[key] >= 2:
            continue
        section_counts[key] += 1
        candidates.append({**c, "hybrid_score": float(hybrid[idx])})
        if len(candidates) >= RERANK_CANDIDATES:
            break

    if len(candidates) <= top_k:
        return candidates[:top_k]

    # Cross-encoder reranking
    doc_texts = [" ".join(c["text"].split()[:400]) for c in candidates]
    scores    = _rerank_scores(query, doc_texts)
    ranked    = sorted(zip(scores, candidates), key=lambda x: x[0], reverse=True)
    top = []
    for sc, c in ranked[:top_k]:
        c = dict(c)
        c["rerank_score"] = float(sc)
        top.append(c)
    return top


def _build_context(chunks: list) -> str:
    parts = []
    for i, c in enumerate(chunks, 1):
        act = c.get("act_title", "Unknown Act")
        sec = c.get("section_title") or c.get("section_number", "")
        parts.append(f"[{i}] {act} -- {sec}\n{c['text'].strip()}")
    return "\n\n".join(parts)


# ── Generation ─────────────────────────────────────────────────────────────────

def _clean_output(text: str) -> str:
    text = re.split(r'\n?\s*\[(?:S|s)ou?r?c?e?\s*[:\;]', text)[0]
    text = re.sub(r'```+.*', '', text, flags=re.DOTALL)
    text = re.sub(r'\s*\|\^\d+\|', '', text)
    text = re.sub(r'([a-z])([A-Z])', r'\1 \2', text)
    text = re.sub(r'([a-zA-Z])(\d)', r'\1 \2', text)
    text = re.sub(r'(\d)([a-zA-Z])', r'\1 \2', text)
    text = text.replace('Â²', '2').replace('Â³', '3')
    text = re.sub(r' {2,}', ' ', text)
    return text.strip()


def _generate_groq(question: str, context: str) -> str:
    client = _state["lm_client"]
    response = client.chat.completions.create(
        model=GROQ_MODEL,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user",   "content": f"Legal Context:\n{context}\n\nQuestion: {question}"},
        ],
        temperature=0.2,
        max_tokens=800,
    )
    return response.choices[0].message.content.strip()


def _generate_local(question: str, context: str) -> str:
    llm    = _state["llm"]
    prompt = (
        f"<s>[INST] {SYSTEM_PROMPT}\n\n"
        f"Legal Context:\n{context}\n\n"
        f"Question: {question} [/INST]"
    )
    out = llm(
        prompt,
        max_tokens=800,
        temperature=0.3,
        top_p=0.9,
        repeat_penalty=1.15,
        stop=["</s>", "[INST]"],
    )
    return _clean_output(out["choices"][0]["text"])


def _generate_ollama(question: str, context: str) -> str:
    import urllib.request, json as _json
    # Use /api/generate with raw Mistral [INST] format to match fine-tuning format.
    # The chat endpoint re-formats prompts in ways that confuse fine-tuned models.
    has_context = context and context != "No specific provisions retrieved."
    if has_context:
        raw_prompt = (
            f"<s>[INST] You are a Pakistani legal expert. "
            f"Answer the question in detail using the legal context below. "
            f"Do not tell the user to rephrase their question.\n\n"
            f"Legal Context:\n{context}\n\n"
            f"Question: {question} [/INST]"
        )
    else:
        raw_prompt = (
            f"<s>[INST] You are a Pakistani legal expert. "
            f"Answer the following question in detail based on your knowledge of Pakistani law.\n\n"
            f"Question: {question} [/INST]"
        )
    payload = _json.dumps({
        "model": OLLAMA_MODEL,
        "prompt": raw_prompt,
        "raw": True,
        "stream": False,
        "options": {"temperature": 0.7, "num_predict": 1024, "repeat_penalty": 1.2},
    }).encode()
    req = urllib.request.Request(
        f"{OLLAMA_BASE_URL}/api/generate",
        data=payload,
        headers={"Content-Type": "application/json"},
    )
    with urllib.request.urlopen(req, timeout=600) as resp:
        result = _json.loads(resp.read())
    return result["response"].strip()


# ── Schemas ────────────────────────────────────────────────────────────────────

class AnswerRequest(BaseModel):
    query: str
    top_k: int = DEFAULT_TOP_K
    user_id: str | None = None
    session_id: str | None = None


class AnswerResponse(BaseModel):
    answer: str
    citations: list[str]
    confidence: float
    num_sources: int
    latency_ms: int
    backend: str


# ── Endpoints ──────────────────────────────────────────────────────────────────

@app.post("/answer", response_model=AnswerResponse)
async def answer_question(
    req: AnswerRequest,
    authorization: str | None = Header(default=None),
):
    _verify_token(authorization)
    if not req.query.strip():
        raise HTTPException(status_code=400, detail="query cannot be empty")

    t0    = time.time()
    top_k = max(1, min(req.top_k or DEFAULT_TOP_K, 10))

    retrieved = _retrieve(req.query, top_k)
    context   = _build_context(retrieved) if retrieved else "No specific provisions retrieved."

    if BACKEND == "groq":
        answer_text = _generate_groq(req.query, context)
    elif BACKEND == "ollama":
        answer_text = _generate_ollama(req.query, context)
    else:
        answer_text = _generate_local(req.query, context)
    latency     = int((time.time() - t0) * 1000)

    citations, seen = [], set()
    for c in retrieved:
        act  = c.get("act_title", "").strip()
        sec  = (c.get("section_title") or c.get("section_number", "")).strip()
        cite = f"{act}, {sec}" if (act and sec) else (act or sec)
        if cite and cite not in seen:
            citations.append(cite)
            seen.add(cite)

    if retrieved:
        raw_score = retrieved[0].get("rerank_score", retrieved[0].get("hybrid_score", 0.5))
        if "rerank_score" in retrieved[0]:
            confidence = round(1.0 / (1.0 + math.exp(-float(raw_score))), 4)
        else:
            confidence = round(min(max(float(raw_score), 0.0), 1.0), 4)
    else:
        confidence = 0.5

    return AnswerResponse(
        answer=answer_text,
        citations=citations,
        confidence=confidence,
        num_sources=len(retrieved),
        latency_ms=latency,
        backend=BACKEND,
    )


