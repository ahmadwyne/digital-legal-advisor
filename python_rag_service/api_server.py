@asynccontextmanager
async def lifespan(app: FastAPI):
    print(f"\n=== Digital Legal Advisor RAG Service  [backend: {BACKEND.upper()}] ===")

    # 1. Load vector store
    store_dir = Path(__file__).parent / VECTOR_STORE_DIR
    if not store_dir.exists():
        raise RuntimeError(
            f"Vector store not found at '{store_dir}'.\n"
            f"Copy vector_store_v2/ into {Path(__file__).parent}/"
        )
    index = faiss.read_index(str(store_dir / "legal_docs.index"))
    with open(store_dir / "chunks_metadata.json", encoding="utf-8") as fh:
        chunks = json.load(fh)
    bm25_path = store_dir / "bm25_corpus.json"
    if bm25_path.exists():
        with open(bm25_path, encoding="utf-8") as fh:
            corpus = json.load(fh)
    else:
        corpus = [c["text"].lower().split() for c in chunks]
    bm25 = BM25Okapi(corpus)
    print(f"  Vector store : {len(chunks):,} chunks  ({store_dir.name})")

    # 2. Embedding model (BAAI/bge-base-en-v1.5, 768-dim)
    #    Uses transformers + torch directly — avoids sentence_transformers which
    #    crashes Python 3.13 at C level due to a TensorFlow import.
    print(f"  Loading embedding model: {EMBEDDING_MODEL} ...")
    embed_tok = AutoTokenizer.from_pretrained(EMBEDDING_MODEL, token=False)
    embed_mdl = AutoModel.from_pretrained(EMBEDDING_MODEL, token=False)
    embed_mdl.eval()
    print(f"  Embedding    : {EMBEDDING_MODEL}  (768-dim, CPU)")

    # 3. Reranker (cross-encoder/ms-marco-MiniLM-L-6-v2)
    print(f"  Loading reranker: {RERANKER_MODEL} ...")
    rerank_tok = AutoTokenizer.from_pretrained(RERANKER_MODEL, token=False)
    rerank_mdl = AutoModelForSequenceClassification.from_pretrained(
        RERANKER_MODEL, token=False
    )
    rerank_mdl.eval()
    print(f"  Reranker     : {RERANKER_MODEL}")

    # 4. Language model backend
    if BACKEND == "groq":
        if not GROQ_API_KEY:
            raise RuntimeError(
                "GROQ_API_KEY is not set.\n"
                "Get a free key at console.groq.com and add it to .env"
            )
        from groq import Groq
        lm_client = Groq(api_key=GROQ_API_KEY)
        print(f"  LM backend   : Groq  ({GROQ_MODEL})")
        _state["lm_client"] = lm_client

    elif BACKEND == "local":
        if not GGUF_PATH:
            raise RuntimeError("GGUF_PATH is not set in .env")
        gguf_file = Path(GGUF_PATH)
        if not gguf_file.is_file():
            raise RuntimeError(f"GGUF model not found: '{gguf_file}'")
        try:
            from llama_cpp import Llama
        except ImportError:
            raise RuntimeError(
                "llama-cpp-python is not installed.\n"
                "Run: pip install llama-cpp-python"
            )
        llm = Llama(
            model_path=str(gguf_file),
            n_ctx=4096,
            n_threads=max(1, (os.cpu_count() or 4) - 1),
            n_gpu_layers=0,
            verbose=False,
        )
        print(f"  LM backend   : local GGUF  ({gguf_file.name})")
        _state["llm"] = llm

    elif BACKEND == "ollama":
        import urllib.request
        try:
            urllib.request.urlopen(f"{OLLAMA_BASE_URL}/api/tags", timeout=3)
        except Exception:
            raise RuntimeError(
                f"Ollama is not reachable at {OLLAMA_BASE_URL}.\n"
                "Make sure Ollama is running."
            )
        print(f"  LM backend   : Ollama  ({OLLAMA_MODEL}  @ {OLLAMA_BASE_URL})")

    else:
        raise RuntimeError(f"Unknown BACKEND '{BACKEND}'. Use 'groq', 'local', or 'ollama'.")

    _state.update(
        index=index, chunks=chunks, bm25=bm25,
        embed_tok=embed_tok, embed_mdl=embed_mdl,
        rerank_tok=rerank_tok, rerank_mdl=rerank_mdl,
    )
    print("=== Service ready ===\n")
    yield
    _state.clear()


